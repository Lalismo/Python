import math

#Redondea hacia abajo
resultado = math.floor(45.3434)
print(resultado)

#Redondea hacia arriba
resultado = math.ceil(45.3434)
print(resultado)

#Metodo pi
resultado = math.pi
print(resultado)

#Metodo logaritmo
resultado =  math.log(25,5)
print(resultado)

#Metodo tangente
resultado = math.tan(44875)
print(resultado)

#Metodo conseno
resultado = math.cos(22344)
print(resultado)