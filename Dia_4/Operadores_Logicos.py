#Operador AND
#Para que nos arroje un valor verdadero se tienen que cumplir las 2 condiciones
mi_bool = 4 < 5 and 5 > 6
print(mi_bool)

#Operador OR
#Para que nos arroje un valor verdadero almenos 1 de las condiciones debe ser verdadera
mi_bool = 5 == 5 or 7 != 7
print(mi_bool)

#Operador NOT
#sería una doble condición ya que si queremos encontrar algo que no sea verdadero, nos tendria que arrojar falso y viseversa
mi_bool = not 5 == 5
print(mi_bool)